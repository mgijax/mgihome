# help section -- fill in Help Url
HELP = string.join (
	[
	'      <A HREF="%%s"><IMG SRC="%simages/shared/help_large.jpg" ',
	'	 BORDER=0 ',
	'        WIDTH=32 HEIGHT=30></A>',
	],
	'\n') % config['TOPURL']


def yourInputSection (
	your_input	# tuple of (string MGI ID, string database dump date)
	):
	# Purpose: private function - generates the Your Input section of a
	#	web page, according to the given parameter 'your_input'
	# Returns: string
	# Assumes: nothing
	# Effects: nothing
	# Throws: nothing

	if your_input == None:
		return '&nbsp;'
	accID, dbDate = your_input
	return YOUR_INPUT % (config['MGIHOMEURL'], accID, dbDate)


def helpSection (
	helpUrl		# string; URL to the help page for this web page
	):
	# Purpose: private function - generates the Help graphic and link for
	#	a web page, according to the given 'helpUrl'
	# Returns: string
	# Assumes: nothing
	# Effects: nothing
	# Throws: nothing

	if helpUrl == None:
		return '&nbsp;'
	return  HELP % helpUrl


def headerBar (
	dataset_id,		# string; brief ID for the data type
	format = None,		# string; identifies the format of the page
	additional = None,	# string; extra line of info about the page
	your_input = None,	# tuple of (string mgi id, string db date)
	do_logo = None		# if non-None, use MGI logo not help link
	):
	# Purpose: returns the header bar at the top of the WI's right column.
	#	Most, if not all, CGI scripts should call header() instead.
	# Returns: string
	# Assumes: nothing
	# Effects: nothing
	# Throws: nothing

	helpUrl = None
	pageTitle = dataset_id		# default - title is passed in
	pageFormat = ''
	moreInfo = ''

	if HELP_URL.has_key (dataset_id):
		helpUrl = HELP_URL[dataset_id]
	if PAGE_TITLE.has_key (dataset_id):
		pageTitle = PAGE_TITLE[dataset_id]
	if PAGE_FORMAT.has_key (format):
		pageFormat = '<BR>%s' % PAGE_FORMAT[format]
	if additional:
		moreInfo = '<BR><FONT SIZE=-1>%s</FONT>' % additional

	# if we need to use a logo, then put in the MGI logo, otherwise
	# give a link to a help doc if available

	if do_logo:
		left_column = ('<IMG SRC="%simages/shared/mgi_logo.jpg" ' +
			'WIDTH=160 HEIGHT=80>') % config['TOPURL']
	else:
		left_column = helpSection (helpUrl)

	return HEADER % (left_column,
			pageTitle + pageFormat + moreInfo,
			yourInputSection (your_input) )

def header (
	dataset_id,		# string; brief ID for the data type
	format = None,		# string; identifies the format of the page
	additional = None,	# string; extra line of info about the page
	your_input = None,	# tuple of (string mgi id, string db date)
	do_logo = None		# if non-None, use MGI logo not help link
	):
	# Purpose: provides the "header" inside the BODY section of a page,
	#	including both the toolbar and the header row.  must call
	#	footer() at the end to properly close the page.
	# Returns: string
	# Assumes: nothing
	# Effects: nothing
	# Throws: nothing

	return bodyStart() + \
	    headerBar (dataset_id, format, additional, your_input, do_logo)

def footer ():
	# Purpose: returns the standard footer for the WI
	# Returns: empty string
	# Assumes: nothing
	# Effects: nothing
	# Throws: nothing
	# Notes: closes the cell, row, and table that we opened in header()

	return bodyStop()



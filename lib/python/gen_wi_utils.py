### --- BEGIN TOOLBAR SECTION -------------------------------------------- ###

def getWideHeader (title = 'Insert Page Title Here'):
	# Purpose: gets a page header for the given title, for a page not
	#	using the toolbar (needing the MGI logo instead)
	# Returns: string
	# Assumes: nothing
	# Effects: nothing
	# Throws: nothing

	return headerBar (title, do_logo = 1)

def getWideFooter ():
	# Purpose: gets the footer as generated by the Java side of the WI
	# Returns: string
	# Assumes: the Java side of the WI is up and running
	# Effects: reads the footer via an http connection
	# Throws: nothing

	FooterUrl = os.path.join (config['JAVA_WI'], 'WIFetch?page=footer')
	lines, errors = httpReader.getURL (FooterUrl)
	if errors:
		return ''

	# note that HTTP header information is returned as well, so the actual
	# footer begins after the first empty line

	lines = map (string.strip, lines)
	if '' in lines:
		pos = lines.index ('')
		lines = lines[pos+1:]

	return string.join (lines, '\n')

def getToolbar ():
	# Purpose: gets the toolbar as generated by the Java side of the WI
	# Returns: string
	# Assumes: the Java side of the WI is up and running
	# Effects: reads the toolbar via an http connection
	# Throws: nothing

	ToolbarUrl = os.path.join (config['JAVA_WI'], 'WIFetch?page=toolbar')
	lines, errors = httpReader.getURL (ToolbarUrl)
	if errors:
		return ''

	# note that HTTP header information is returned as well, so the actual
	# toolbar begins after the first empty line

	lines = map (string.strip, lines)
	if '' in lines:
		pos = lines.index ('')
		lines = lines[pos+1:]

	return string.join (lines, '\n')


def toolbar ():
	# Purpose: returns the version of the toolbar which is cached as an
	#	include file (if available) or an empty string (if not)
	# Returns: string
	# Assumes: the file (include/toolbar.html) exists
	# Effects: reads from the file system
	# Throws: nothing

	tbPath = os.path.join (config['TOPFULLPATH'],
				'www/include/toolbar.html')
	if not os.path.exists (tbPath):
		return ''
	fp = open (tbPath, 'r')
	lines = fp.readlines()
	fp.close()
	return string.join (lines, '')

### --- END TOOLBAR SECTION ---------------------------------------------- ###

def bodyStart ():
	# Purpose: provides the opening for a WI page, including the toolbar
	#	and leaving the right-hand table cell open.  Most, if not all,
	#	CGI scripts should call header() instead.
	# Returns: string
	# Assumes: nothing
	# Effects: nothing
	# Throws: nothing

	return '<TABLE WIDTH="100%">' + \
		'<TR WIDTH="100%"><TD WIDTH="15%" VALIGN="top">' + \
			toolbar() + \
		'</TD><TD WIDTH="1" BGCOLOR="#888888">&nbsp;' + \
		'</TD><TD WIDTH="*" VALIGN="top">'

def bodyStop ():
	# Purpose: returns the string of HTML needed to properly close the
	#	tags left open by bodyStart().  Most, if not all, CGI scripts
	#	should call footer() instead.
	# Returns: string
	# Assumes: nothing
	# Effects: nothing
	# Throws: nothing

	return "</TD></TR></TABLE>"

